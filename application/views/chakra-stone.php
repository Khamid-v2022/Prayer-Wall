                           Located at the base of the spine, the root chakra is associated with stability, grounding, and a sense of security. Red Jasper or Hematite, known for their grounding properties, help in stabilizing this chakra. These stones encourage physical health, promote courage, and contribute to a sense of safety.
                        </li>
                        <li>
                            <strong><a class="text-decoration-none" href="https://angelgraceblessing.com/sacral-chakra-affirmation-i-am-open-to-pleasure-and-abundance-in-all-areas-of-my-life/" target="_blank">The Sacral Chakra</a>:</strong><br>
                            The sacral chakra, positioned just below the navel, governs creativity, sensuality, and emotional well-being. Orange Carnelian or Sunstone can enhance the flow of energy in this chakra, encouraging inspiration, passion, and a balanced emotional state.
                        </li>
                        <li>
                            <strong><a class="text-decoration-none" href="https://angelgraceblessing.com/solar-plexus-chakra-affirmation-i-can-achieve-anything-i-set-my-mind-to" target="_blank">The Solar Plexus Chakra</a>:</strong><br>
