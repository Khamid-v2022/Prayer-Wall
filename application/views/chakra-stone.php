                        </li>
                        <li>
                            <strong><a class="text-decoration-none" href="https://angelgraceblessing.com/heart-chakra-affirmation-i-trust-in-the-power-of-love-to-guide-and-heal-my-heart" target="_blank">The Heart Chakra</a>:</strong><br>
                            Situated in the center of the chest, the heart chakra governs love, compassion, and emotional healing. Green Aventurine or Rose Quartz are commonly used to open and balance this chakra, fostering feelings of love, harmony, and empathy.
                        </li>
                        <li>
                            <strong><a class="text-decoration-none" href="https://angelgraceblessing.com/throat-chakra-affirmation-i-express-myself-authentically-and-with-confidence" target="_blank">The Throat Chakra</a>:</strong><br>
