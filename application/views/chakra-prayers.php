                            Centered at the base of the spine, this chakra grounds us and connects us to the earth. A prayer might be, "I am grounded, safe, and secure. I trust the flow of life".
                        </li>
                        <li>
                            <a href="https://angelgraceblessing.com/sacral-chakra-prayer-for-creativity-and-emotional-balance/"><strong>Sacral Chakra Prayer:</strong></a><br>
                             Located in the lower abdomen, this chakra governs creativity and emotions. A suitable prayer could be, "I embrace my creativity and sensuality. I am open to joy and pleasure."
                        </li>
                        <li>
                           <a href="https://angelgraceblessing.com/solar-plexus-chakra-prayer-for-personal-power-and-confidence/"><strong>Solar Plexus Chakra Prayer:</strong></a><br>
                            Situated above the navel, this chakra is linked to personal power and confidence. An affirmation might be, "I am strong, confident, and in control of my life."
                        </li>
                        <li>
                            <a href="https://angelgraceblessing.com/heart-chakra-prayer-for-love-and-compassion/"><strong>Heart Chakra Prayer:</strong></a><br>
